using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SimpleCalc.MainClasses
{
    class MainClass
    {
        //create add method 
        float addMethod(float x,float y)
        {
            return x + y;
        }
        //create subtract method 
        float subMethod(float x, float y)
        {
            return x - y;
        }
        //create multiply method 
        float mulMethod(float x, float y)
        {
            return x * y;
        }
        //create divide method 
        float divMethod(float x, float y)
        {
            return x / y;
        }
       
    }
}
